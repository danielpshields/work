    <h3>Career Changer Program</h3>
    <p>This program is specifically for students who have not taken science pre-requisite courses (maximum of two completed).</p>
    <p><b>Eligibility:</b></p>
    <ul>
      <li>Baccalaureate degree (or higher) from a regionally accredited college or university,</li>
      <li>Minimum cumulative and upper-division GPAs of 3.0. Most students who are successful in gaining admission to medical, dental, and veterinary medicine schools have GPAs of 3.5 and higher.</li>
      <li>MAC1147 Pre-Calculus and CHM1025 Intro to Chemistry or their equivalents with a minimum grade of B or above prior to matriculation. Applicants may take these courses online or in-residence through UF prior to starting PHPB. Contact us for more information.</li>
      <li>Have a minimum GPA of 3.0 in any pre-professional science courses already completed.</li>
      <li>Have worked or volunteered in a clinical setting or in another service related capacity prior to application.</li>
    </ul>

    <h3>Pre-Health ReStart Program</h3>
    <p>This track is specifically for:</p>
    <p class="squeezed">Students who took science pre-requisite courses (3 or less) early in undergraduate and earned less than a B and stopped taking science courses. <b><u>No attempts in science courses for a minimum of two years prior to application.</u></b></p>

    <p class="center"><b><u>OR</u></b></p>

    <p class="squeezed">Students who took the pre-requisite science courses greater than five years ago.</p>
    <p>This is not an academic record enhancing program for students who completed all of the pre-requisite courses.</p>
    <p><b>All courses five or more years old and/or with less than a B earned are required to be repeated.</b></p>
    <p><b>Eligibility:</b></p>
    <ul>
      <li>Baccalaureate degree (or higher) from a regionally accredited college or university.</li>
      <li>Cumulative GPA of 3.0 or higher.</li>
      <li>Be able to articulate the reasons for previous academic difficulty.</li>
      <li>Complete CHM1025 and MAC1147 <b><u>at UF</u></b> prior to matriculation in the program. Grades of "B" or better must be earned to meet admissions conditions. Online enrollment may be possible. Please contact us for more information.</li>
      <li>Upon admission students in the ReStart program will have additional Admission Conditions to complete in the summer prior to starting fall semester. These will be indicated on the letter of acceptance.</li>
    </ul>
    <p>Students admitted to the ReStart track will be expected to participate in tutoring and other supportive services offered by PHPB.</p>

