<h3>When should I make an advising appointment?</h3>
<ul>
  <li>You are a newly admitted UF Online student needing an initial appointment with your advisor</li>
  <li>You are seeking readmission to the UF Online program</li>
  <li>You need to discuss a hold such as probation or a registration hold</li>
  <li>You have questions about changing to a CLAS UF Online major</li>
  <li>You are filing a petition and need an interviewing officer statement</li>
</ul>

<h3>When should I utilize call-in advising?</h3>
<ul>
  <li>You have a quick, advising-related question</li>
  <li>You cannot register for a course because of a pre-requisite issue</li>
  <li>You want to discuss a tracking hold</li>
  <li>You would like a quick review of your course schedule</li>
</ul>

