    <h2>Variables</h2>
    <ul>
      <li>`const` is a signal that the identifier won’t be reassigned. (typically all caps)</li>
      <li>`let`, scoped: blocked | is a signal that the variable may be reassigned, such as a counter in a loop, or a value swap in an algorithm. It also signals that the variable will be used only in the block it’s defined in, which is not always the entire containing function.</li>
      <li>`var`, scoped: function</li>
    </ul>

