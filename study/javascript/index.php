    <h1>DOM</h1>
    <p>The DOM is often referred to as the DOM tree, and consists of a tree of objects called nodes</p>
    <h2>JavaScript</h2>
    <p>JavaScript is the client-side scripting language that connects to the DOM in an internet browser.</p>
    <p>The DOM is language agnostic, or created to be independent from a particular programming language</p>
    <p>In addition to parsing the style and structure of the HTML and CSS, the browser creates a representation of the document known as the Document Object Model. This model allows JavaScript to access the text content and elements of the website document as objects</p>
    <p>document is an object, body is a property of that object that we have accessed with dot notation. "document.body"</p>
